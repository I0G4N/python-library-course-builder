export const COURSE_COPY = Object.freeze({ "zh-CN": zhCN, en });

export function resolveCourseLanguage(schemaVersion, language) {
  const resolvedSchemaVersion = schemaVersion === undefined ? 2 : schemaVersion;
  if (
    !Number.isInteger(resolvedSchemaVersion) ||
    (resolvedSchemaVersion !== 2 && resolvedSchemaVersion !== 3)
  ) {
    throw new Error("manifest.schema_version must be 2 or 3");
  }
  if (resolvedSchemaVersion === 2) {
    return language === "zh-CN" || language === "en" ? language : "zh-CN";
  }
  if (resolvedSchemaVersion === 3) {
    if (language === "zh-CN" || language === "en") return language;
    throw new Error(
      language == null || language === ""
        ? "schema v3 manifest.language is required"
        : `schema v3 manifest.language is unsupported: ${language}`,
    );
  }
  throw new Error("unreachable course schema version");
}

export function courseCopy(language) {
  const copy = COURSE_COPY[language];
  if (!copy) throw new Error(`unsupported course language: ${language}`);
  return copy;
}
